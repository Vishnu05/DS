package Java.Basics.Loops;

public class ForLoops {

    public static void main(String args[]) {
        double b = 25;
        for (long a = 0; a < b; a++)
            System.out.println(a);
        int a = 1;
        System.out.println("i really don't have a idea " + a);

        int i = 0;
        for (i = 0; i < 5; i++) {
            System.out.println(i);
        }
        System.out.println(i);
    }
}